package cn.ac.big.bigd.webservice.model.screen;

public class DataComposition {
    private int microbeDataSets;
    private double microbeFileSize;
    private int animalDataSets;
    private double animalFileSize;
    private int humanDataSets;
    private double humanFileSize;
    private int plantDataSets;
    private double plantFileSize;

    public int getMicrobeDataSets() {
        return microbeDataSets;
    }

    public void setMicrobeDataSets(int microbeDataSets) {
        this.microbeDataSets = microbeDataSets;
    }

    public double getMicrobeFileSize() {
        return microbeFileSize;
    }

    public void setMicrobeFileSize(double microbeFileSize) {
        this.microbeFileSize = microbeFileSize;
    }

    public int getAnimalDataSets() {
        return animalDataSets;
    }

    public void setAnimalDataSets(int animalDataSets) {
        this.animalDataSets = animalDataSets;
    }

    public double getAnimalFileSize() {
        return animalFileSize;
    }

    public void setAnimalFileSize(double animalFileSize) {
        this.animalFileSize = animalFileSize;
    }

    public int getHumanDataSets() {
        return humanDataSets;
    }

    public void setHumanDataSets(int humanDataSets) {
        this.humanDataSets = humanDataSets;
    }

    public double getHumanFileSize() {
        return humanFileSize;
    }

    public void setHumanFileSize(double humanFileSize) {
        this.humanFileSize = humanFileSize;
    }

    public int getPlantDataSets() {
        return plantDataSets;
    }

    public void setPlantDataSets(int plantDataSets) {
        this.plantDataSets = plantDataSets;
    }

    public double getPlantFileSize() {
        return plantFileSize;
    }

    public void setPlantFileSize(double plantFileSize) {
        this.plantFileSize = plantFileSize;
    }
}
